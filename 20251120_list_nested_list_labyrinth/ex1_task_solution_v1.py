# Распечатка элементов таблицы, основанной на списках в списке:
#     не так:
#         [[1, 2, 6, 4], [0, 5, 5, 1], [7, 7, 8, 6]]
# 
#     не так:
#         [1, 2, 6, 4]
#         [0, 5, 5, 1]
#         [7, 7, 8, 6]
# 
#     а так:
#         1   2   6   4
#         0   5   5   1
#         7   7   8   6

table = [[1, 2, 6, 4], [0, 5, 5, 1], [7, 7, 8, 6]]

for y in range(len(table)):
    for x in range(len(table[y])):
        print(table[y][x], end=" ")
    print()
